# ตัวแปร num_base10 เก็บเลขจำนวนเต็มฐานสิบ
num_base10 = 10
# ตัวแปร num_base2 เก็บเลขจำนวนเต็มฐานสอง
num_base2 = bin(10)
# ตัวแปร num_base8 เก็บเลขจำนวนเต็มฐานcxf
num_base8 = oct(10)
# ตัวแปร num_base16 เก็บเลขจำนวนเต็มฐานสิบหก
num_base16 = hex(10)
print(num_base10)
print(num_base2)
print(num_base8)
print(num_base16)


